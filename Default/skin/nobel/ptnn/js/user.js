$(function() {
	$('#menu_user').mouseover(function() {
		$('.menu_user').show();
	});
	$('#menu_user').mouseout(function() {
		$('.menu_user').hide();
	});
	$('.menu_user').mouseout(function() {
		$('.menu_user').hide();
	});
	$('.menu_user').mouseover(function() {
		$('.menu_user').show();
	});	
});
